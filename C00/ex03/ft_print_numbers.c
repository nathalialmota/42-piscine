#include <unistd.h>

void	ft_print_numbers(void)
{
	char	number;

	number = '0';
	while (number <= '9')
	{
		write(1, &number, 1);
		number++;
	}
}

int main()
{
    ft_print_numbers();
    write(1, "\n", 1);
    return 0;
}
